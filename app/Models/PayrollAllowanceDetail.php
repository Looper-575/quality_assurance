<?php

namespace App\Models;

use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;

class PayrollAllowanceDetail extends Authenticatable
{
    use HasFactory, Notifiable;

    protected $table = 'payroll_allowance_details';
    protected $primaryKey = 'id';
    public $timestamps = false;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'payroll_id', 'title', 'amount',
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [

    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'added_on' => 'datetime',
        'modified_on' => 'datetime',
    ];

    public function payroll()
    {
        return $this->belongsTo(Payroll::class , 'payroll_id' , 'payroll_id');
    }

    public function income_tax()
    {
        return $this->belongsTo(Payroll::class , 'payroll_id' , 'payroll_id')->where('title', 'Income Tax');
    }
}
